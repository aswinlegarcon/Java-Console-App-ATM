package ATM.Notes;

public class Notes {
    private String note;
    private int count;


    public void setNote(String note)
    {
        this.note = note;
    }

    public String getNote()
    {
        return this.note;
    }

    public void setCount(int count)
    {
        this.count = count;
    }

    public int getCount()
    {
        return this.count;
    }


}